Protocol.Checksum = function (s)
	local value = s[5];
	for i = 6, 8 do
		value = bits.bxor(value,s[i]);
	end;
	s[9] = math.mod(value, 256);
	return s;
end;


Protocol.CamAddrProcess = function(s, addr)
	s[6] = addr - 1;
	return s;
end;

Protocol.AuxProcess = function(s,num)
	return s;
end;

Protocol.SpeedProcess = function(s,num)
	return s;
end;

return Protocol;